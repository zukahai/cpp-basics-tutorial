#include <bits/stdc++.h>

using namespace std;

int main() {
	int a, b, c;
	cin >> a >> b >> c;
	cout << a % 10 << endl;
	cout << b % 10 << endl;
	cout << c % 10 << endl;
	cout << (a + b) % 10 << endl;
	cout << (a + c) % 10 << endl;
	cout << (b + c) % 10 << endl;
	cout << (a + b + c) % 10 << endl;
}

#include <bits/stdc++.h>

using namespace std;

int main() {
	int a, b;
	cin >> a >> b;
	cout << "Phan nguyen cua " << a << " / " << b << " la " << a / b << endl;
	cout << "Phan du cua " << a << " / " << b << " la " << a % b << endl;
	cout << "Trung binh cong cua " << a << " va " << b << " la " << (a + b) / 2.0;
}
